require_non_empty() {
  local value="$1"
  local name="$2"
  if [[ -z "${value}" ]]; then
    echo "Error: ${name} is required." >&2
    exit 1
  fi
}

escape_sed_repl() {
  printf '%s' "$1" | sed -e 's/[\/&]/\\&/g'
}

upsert_env_key() {
  local file="$1"
  local key="$2"
  local value="$3"
  local escaped
  escaped="$(escape_sed_repl "${value}")"

  if [[ -f "${file}" ]] && grep -q "^${key}=" "${file}"; then
    sed -i -E "s#^${key}=.*#${key}=${escaped}#" "${file}"
  else
    {
      if [[ ! -f "${file}" ]]; then
        echo "# Local environment values (generated by scaffold-env.sh)"
      fi
      echo "${key}=${value}"
    } >> "${file}"
  fi
}

maybe_discover_defaults() {
  local discovery_script discovery_file
  discovery_script="${SCRIPT_DIR}/discover-proxmox-core.sh"

  if [[ "${AUTO_DISCOVER}" != true ]]; then
    return 0
  fi
  if [[ -z "${PROXMOX_HOST}" ]]; then
    echo "Warning: AUTO_DISCOVER=true but PROXMOX_HOST is empty; skipping discovery." >&2
    return 0
  fi
  if [[ ! -x "${discovery_script}" ]]; then
    echo "Warning: discovery script missing or not executable: ${discovery_script}" >&2
    return 0
  fi

  discovery_file="$(mktemp)"
  if ! "${discovery_script}" --host "${PROXMOX_HOST}" --user "${PROXMOX_USER}" --env > "${discovery_file}"; then
    echo "Warning: auto-discovery failed for ${PROXMOX_USER}@${PROXMOX_HOST}; continuing with provided/manual values." >&2
    rm -f "${discovery_file}"
    return 0
  fi

  # shellcheck disable=SC1090
  set -a
  source "${discovery_file}"
  set +a

  rm -f "${discovery_file}"
}

rewrite_network() {
  local file="$1"
  local cidr="$2"
  local gw="$3"

  if [[ -n "${cidr}" ]]; then
    local ip prefix base
    ip="${cidr%/*}"
    prefix="${cidr#*/}"
    base="$(awk -F. '{print $1"."$2"."$3}' <<< "${ip}")"

    if [[ "${ip}" =~ ^[0-9]+\.[0-9]+\.[0-9]+\.[0-9]+$ ]] && [[ "${prefix}" =~ ^[0-9]+$ ]]; then
      sed -i -E "s#ip=[0-9]+\.[0-9]+\.[0-9]+\.([0-9]+)/[0-9]+#ip=${base}.\\1/${prefix}#g" "${file}"
    else
      echo "Warning: NETWORK_CIDR '${cidr}' is not valid IPv4 CIDR; skipping ipconfig0 rewrite." >&2
    fi
  fi

  if [[ -n "${gw}" ]]; then
    if [[ "${gw}" =~ ^[0-9]+\.[0-9]+\.[0-9]+\.[0-9]+$ ]]; then
      sed -i -E "s#gw=[0-9]+\.[0-9]+\.[0-9]+\.[0-9]+#gw=${gw}#g" "${file}"
    else
      echo "Warning: NETWORK_GW '${gw}' is not a valid IPv4 address; skipping gateway rewrite." >&2
    fi
  fi
}

set_data_disk_storage_in_tfvars() {
  local file="$1"
  local value="$2"
  local tmp
  tmp="$(mktemp)"
  awk -v storage_value="${value}" '
    BEGIN { in_block = 0; updated = 0 }
    /^[[:space:]]*data_disk_defaults[[:space:]]*=[[:space:]]*{/ { in_block = 1; print; next }
    in_block && /^[[:space:]]*}/ { in_block = 0; print; next }
    in_block && /^[[:space:]]*storage[[:space:]]*=/ {
      print "  storage = \"" storage_value "\""
      updated = 1
      next
    }
    { print }
    END {
      if (updated == 0) {
        # no-op; caller can decide if this is expected.
      }
    }
  ' "${file}" > "${tmp}"
  mv "${tmp}" "${file}"
}

set_string_tfvar() {
  local file="$1"
  local key="$2"
  local value="$3"
  local tmp

  tmp="$(mktemp)"
  awk -v tf_key="${key}" -v tf_value="${value}" '
    BEGIN { updated = 0 }
    $0 ~ "^[[:space:]]*" tf_key "[[:space:]]*=" {
      print tf_key " = \"" tf_value "\""
      updated = 1
      next
    }
    { print }
    END {
      if (updated == 0) {
        print tf_key " = \"" tf_value "\""
      }
    }
  ' "${file}" > "${tmp}"
  mv "${tmp}" "${file}"
}

derive_network_cidr_from_host_cidr() {
  local host_cidr="$1"
  local host_ip prefix
  host_ip="${host_cidr%/*}"
  prefix="${host_cidr#*/}"

  if command -v ipcalc >/dev/null 2>&1; then
    ipcalc -n "${host_cidr}" 2>/dev/null | awk -F': *' '/^Network:/ {print $2; exit}'
    return 0
  fi

  if [[ "${host_ip}" =~ ^[0-9]+\.[0-9]+\.[0-9]+\.[0-9]+$ && "${prefix}" == "24" ]]; then
    awk -F. -v p="${prefix}" '{print $1"."$2"."$3".0/"p}' <<< "${host_ip}"
    return 0
  fi

  printf '%s\n' ""
}

