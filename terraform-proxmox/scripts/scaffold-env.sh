    sed -i -E "s#^(proxmox_node[[:space:]]*=[[:space:]]*\").*(\"[[:space:]]*)\$#\\1${node_escaped}\\2#" "${target_vars}"
  fi

  case "${packer_dir}" in
    ubuntu-noble)
      template_name="${TEMPLATE_NAME_UBUNTU2404}"
      template_vmid="${TEMPLATE_VMID_UBUNTU2404}"
      clone_vmid="${BASE_VMID_UBUNTU2404}"
