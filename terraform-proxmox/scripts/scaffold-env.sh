      clone_vmid="${BASE_VMID_ORACLE9}"
      ;;
    *)
      template_name=""
      template_vmid=""
      clone_vmid=""
      ;;
  esac

  if [[ -n "${template_name}" ]]; then
    template_name_escaped="$(escape_sed_repl "${template_name}")"
    sed -i -E "s#^(template_name[[:space:]]*=[[:space:]]*\").*(\"[[:space:]]*)\$#\\1${template_name_escaped}\\2#" "${target_vars}"
  fi
  if [[ -n "${template_vmid}" ]]; then
    sed -i -E "s#^(vm_id[[:space:]]*=[[:space:]]*).*\$#\\1${template_vmid}#" "${target_vars}"
  fi
  if [[ -n "${clone_vmid}" ]]; then
    sed -i -E "s#^(clone_vm_id[[:space:]]*=[[:space:]]*).*\$#\\1${clone_vmid}#" "${target_vars}"
  fi
done

