if [[ -n "${PROXMOX_NODE}" ]]; then
  upsert_env_key "${env_file}" "PROXMOX_NODE_${env_upper}" "${PROXMOX_NODE}"
fi
if [[ -n "${ANSIBLE_HOST}" ]]; then
  upsert_env_key "${env_file}" "ANSIBLE_HOST_${env_upper}" "${ANSIBLE_HOST}"
fi
